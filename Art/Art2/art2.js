const openBtn1 = document.getElementById('btn-text1');
const modal1 = document.querySelector('.modal1-box')
const close1 = document.getElementById('close-pic1')

const openModal1 = () => {
    modal1.style.display = 'block';
  }

  function closeModal1() {
    modal1.style.display = 'none';
}
  
  //Event Listeners
  openBtn1.addEventListener('click', openModal1)
  
  close1.addEventListener('click', closeModal1)
  
